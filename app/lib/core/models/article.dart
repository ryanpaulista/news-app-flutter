class Article {
  final String id;
  final String title;
  final String author;
  final String postedAt;
  final String imageUrl;
  final String summary;
  final String body;

  const Article({
    required this.id,
    required this.title,
    required this.author,
    required this.postedAt,
    required this.imageUrl,
    required this.summary,
    required this.body,
  });
}
